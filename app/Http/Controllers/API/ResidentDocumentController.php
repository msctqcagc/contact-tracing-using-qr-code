    public function upload($id, Request $request) {
        $imageName = time().'.'.$request->file->extension();
        $request->file->move(public_path('uploads/images/documents/'), $imageName);

        ResidentDocument::create([
            'resident_id' => $id,
            'type' => $request->document_type,
            'file_name' => $imageName
        ]);

        return;
